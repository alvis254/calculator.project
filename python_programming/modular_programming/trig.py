#implement trigonometric functions

import math

#find the sine
def get_sine(x):
    #x is in degrees
    angle_rad = math.radians(x)
    angle_sin = math.sin(angle_rad)

    return angle_sin

    print(x)

def get_cos(x):
    #x is in degrees
    angle_rad = math.radians(x)
    angle_cos = math.cos(angle_rad)

    return angle_cos
    print(x)

def get_tan(x):
    #x is in degrees
    angle_rad = math.radians(x)
    angle_tan = math.tan(angle_rad)

    return angle_tan
    print(x)

